package com.masscustsoft.module;

public class SaveCfg extends ActionCfg {
	String beforeSave, afterSave, module;

	public String getBeforeSave() {
		return beforeSave;
	}

	public void setBeforeSave(String beforeSave) {
		this.beforeSave = beforeSave;
	}

	public String getAfterSave() {
		return afterSave;
	}

	public void setAfterSave(String afterSave) {
		this.afterSave = afterSave;
	}

	public String getModule() {
		return module;
	}

	public void setModule(String module) {
		this.module = module;
	}

}
